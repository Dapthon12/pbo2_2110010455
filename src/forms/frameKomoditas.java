    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if (txtIDKomoditas.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "ID Komoditas belum di isi!");
                txtIDKomoditas.requestFocus();
            } else if (txtNamaKomoditas.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Nama Komoditas belum di isi!");
                txtNamaKomoditas.requestFocus();
            } else if (txtRincianKomoditas.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Rincian Komoditas belum di isi!");
                txtRincianKomoditas.requestFocus();
            } else {
                String sqlCari = "SELECT * FROM komoditas WHERE id_komoditas ='"+txtIDKomoditas.getText()+"'";
                ResultSet cari = myObject.konek.createStatement().executeQuery(sqlCari);
                if(cari.next()){
                    JOptionPane.showMessageDialog(this, "ID Komoditas sudah terdaftar!!!");
                                         
                    txtNamaKomoditas.setText(cari.getString(2));
                    txtRincianKomoditas.setText(cari.getString(3));
                    
                } else {
                    String sql = "INSERT INTO komoditas VALUE (?, ?, ?)";
                    PreparedStatement simpan = myObject.konek.prepareStatement(sql);
                    simpan.setString(1, txtIDKomoditas.getText());
                    simpan.setString(2, txtNamaKomoditas.getText());
                    simpan.setString(3, txtRincianKomoditas.getText());
                    simpan.executeUpdate();
                    tampilDataKomoditas();
                    JOptionPane.showMessageDialog(this, "Data berhasil di simpan");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

