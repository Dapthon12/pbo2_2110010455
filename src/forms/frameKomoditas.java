    private void txtCariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyPressed
        // TODO add your handling code here:
        modelTabelKomoditas.getDataVector().removeAllElements();
        modelTabelKomoditas.fireTableDataChanged();
        try {
            String sqlcari = "SELECT * FROM komoditas WHERE id_komoditas LIKE '%"+txtCari.getText()+"%' "
                        + "OR nama_komoditas LIKE '%"+txtCari.getText()+"%' OR rincian_komoditas LIKE '%"+txtCari.getText()+"%'";
            Statement cari = myObject.konek.createStatement();
            ResultSet data = cari.executeQuery(sqlcari);
            while (data.next()) {                
                Object[] baris = new Object[3];
                baris[0] = data.getString("id_komoditas");
                baris[1] = data.getString("nama_komoditas");
                baris[2] = data.getString("rincian_komoditas");
                modelTabelKomoditas.addRow(baris);   
            }
            cari.close();
            data.close();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_txtCariKeyPressed

