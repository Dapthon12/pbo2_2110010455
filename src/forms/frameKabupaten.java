    private void txtCariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyPressed
        // TODO add your handling code here:
        modelTabelKabupaten.getDataVector().removeAllElements();
        modelTabelKabupaten.fireTableDataChanged();
        try {
            String sqlcari = "SELECT * FROM kabupaten WHERE id_kabupaten LIKE '%"+txtCari.getText()+"%' "
                        + "OR nama_kabupaten LIKE '%"+txtCari.getText()+"%' OR id_provinsi LIKE '%"+txtCari.getText()+"%'";
            Statement cari = myObject.konek.createStatement();
            ResultSet data = cari.executeQuery(sqlcari);
            while (data.next()) {                
                Object[] baris = new Object[3];
                baris[0] = data.getString("id_kabupaten");
                baris[1] = data.getString("nama_kabupaten");
                baris[2] = data.getString("id_provinsi");
                modelTabelKabupaten.addRow(baris);   
            }
            cari.close();
            data.close();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_txtCariKeyPressed

