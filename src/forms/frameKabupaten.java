    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            if (txtIDKabupaten.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "ID kabupaten belum di isi!");
                txtIDKabupaten.requestFocus();
            } else if (txtNamaKabupaten.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Nama kabupaten belum di isi!");
                txtNamaKabupaten.requestFocus();
            } else if (txtIDProvinsi.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "ID Provinsi belum di isi!");
                txtIDProvinsi.requestFocus();
            } else {
                String sqlCari = "SELECT * FROM kabupaten WHERE id_kabupaten ='"+txtIDKabupaten.getText()+"'";
                ResultSet cari = myObject.konek.createStatement().executeQuery(sqlCari);
                if(cari.next()){
                    JOptionPane.showMessageDialog(this, "ID Kabupaten sudah terdaftar!!!");
                                         
                    txtNamaKabupaten.setText(cari.getString(2));
                    txtIDProvinsi.setText(cari.getString(3));
                    
                } else {
                    String sql = "INSERT INTO kabupaten VALUE (?, ?, ?)";
                    PreparedStatement simpan = myObject.konek.prepareStatement(sql);
                    simpan.setString(1, txtIDKabupaten.getText());
                    simpan.setString(2, txtNamaKabupaten.getText());
                    simpan.setString(3, txtIDProvinsi.getText());
                    simpan.executeUpdate();
                    tampilDataKabupaten();
                    JOptionPane.showMessageDialog(this, "Data berhasil di simpan");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

