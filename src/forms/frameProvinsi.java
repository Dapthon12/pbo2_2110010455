    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            if (txtIDProvinsi.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "ID Provinsi belum di isi!");
                txtIDProvinsi.requestFocus();
            } else if (txtNamaProvinsi.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Nama Provinsi belum di isi!");
                txtNamaProvinsi.requestFocus();
            } else {
                String sqlCari = "SELECT * FROM provinsi WHERE id_provinsi ='"+txtIDProvinsi.getText()+"'";
                ResultSet cari = myObject.konek.createStatement().executeQuery(sqlCari);
                if(cari.next()){
                    JOptionPane.showMessageDialog(this, "ID Provinsi sudah terdaftar!!!");
                                         
                    txtNamaProvinsi.setText(cari.getString(2));
                    
                } else {
                    String sql = "INSERT INTO provinsi VALUE (?, ?)";
                    PreparedStatement simpan = myObject.konek.prepareStatement(sql);
                    simpan.setString(1, txtIDProvinsi.getText());
                    simpan.setString(2, txtNamaProvinsi.getText());
                    simpan.executeUpdate();
                    tampilDataProvinsi();
                    JOptionPane.showMessageDialog(this, "Data berhasil di simpan");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

