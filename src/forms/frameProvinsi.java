    public void tampilDataProvinsi() {
        try {
            modelTabelProvinsi.getDataVector().removeAllElements();
            modelTabelProvinsi.fireTableDataChanged();
            
            PreparedStatement query = myObject.konek.prepareStatement("SELECT * FROM provinsi");
            ResultSet data = query.executeQuery();
            while (data.next()) {                
                Object[] baris = new Object[2];
                baris[0] = data.getString("id_provinsi");
                baris[1] = data.getString("nama_provinsi");
                modelTabelProvinsi.addRow(baris);   
            }
            query.close();
            data.close();
        } catch (Exception e) {
        }
    }
    
