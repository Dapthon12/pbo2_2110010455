    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if (txtIDBukaLelang.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "ID Buka lelang belum di isi!");
                txtIDBukaLelang.requestFocus();
            } else if (txtIDPetani.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "ID Petani belum di isi!");
                txtIDPetani.requestFocus();
            } else if (txtIDKomoditas.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "ID Komoditas belum di isi!");
                txtIDKomoditas.requestFocus();
            } else if (txtBeratTotal.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Berat total belum di isi!");
                txtBeratTotal.requestFocus();
            } else if (txtHargaAwal.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Harga awal belum di isi!");
                txtHargaAwal.requestFocus();
            } else if (txtTanggalBuka.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Tanggal buka belum di isi!");
                txtTanggalBuka.requestFocus();
            } else if (txtTanggalHabis.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Tanggal habis belum di isi!");
                txtTanggalHabis.requestFocus();
            } else if (txtWaktuHabis.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Waktu habis belum di isi!");
                txtWaktuHabis.requestFocus();
            } else {
                String sqlCari = "SELECT * FROM buka_lelang WHERE id_bukalelang ='"+txtIDBukaLelang.getText()+"'";
                ResultSet cari = myObject.konek.createStatement().executeQuery(sqlCari);
                if(cari.next()){
                    JOptionPane.showMessageDialog(this, "ID Buka lelang sudah terdaftar!!!");
                                         
                    txtIDPetani.setText(cari.getString(2));
                    txtIDKomoditas.setText(cari.getString(3));
                    txtBeratTotal.setText(cari.getString(4));
                    txtHargaAwal.setText(cari.getString(5));
                    txtTanggalBuka.setText(cari.getString(6));
                    txtTanggalHabis.setText(cari.getString(7));
                    txtWaktuHabis.setText(cari.getString(8));
                    
                } else {
                    String sql = "INSERT INTO buka_lelang VALUE (?, ?, ?, ?, ?, ?, ?, ?)";
                    PreparedStatement simpan = myObject.konek.prepareStatement(sql);
                    simpan.setString(1, txtIDBukaLelang.getText());
                    simpan.setString(2, txtIDPetani.getText());
                    simpan.setString(3, txtIDKomoditas.getText());
                    simpan.setString(4, txtBeratTotal.getText());
                    simpan.setString(5, txtHargaAwal.getText());
                    simpan.setString(6, txtTanggalBuka.getText());
                    simpan.setString(7, txtTanggalHabis.getText());
                    simpan.setString(8, txtWaktuHabis.getText());
                    simpan.executeUpdate();
                    tampilDataBukaLelang();
                    JOptionPane.showMessageDialog(this, "Data berhasil di simpan");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

