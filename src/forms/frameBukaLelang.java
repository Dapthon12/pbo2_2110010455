    private void txtCariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyPressed
        // TODO add your handling code here:
        modelTabelBukaLelang.getDataVector().removeAllElements();
        modelTabelBukaLelang.fireTableDataChanged();
        try {
            String sqlcari = "SELECT * FROM buka_lelang WHERE id_bukalelang LIKE '%"+txtCari.getText()+"%' "
                        + "OR id_petani LIKE '%"+txtCari.getText()+"%' OR id_komoditas LIKE '%"+txtCari.getText()+"%'"
                        + "OR berat_total LIKE '%"+txtCari.getText()+"%' OR harga_awal LIKE '%"+txtCari.getText()+"%'"
                        + "OR tanggal_buka LIKE '%"+txtCari.getText()+"%' OR tanggal_habis LIKE '%"+txtCari.getText()+"%'"
                        + "OR waktu_habis LIKE '%"+txtCari.getText()+"%'";
            Statement cari = myObject.konek.createStatement();
            ResultSet data = cari.executeQuery(sqlcari);
            while (data.next()) {                
                Object[] baris = new Object[8];
                baris[0] = data.getString("id_bukalelang");
                baris[1] = data.getString("id_petani");
                baris[2] = data.getString("id_komoditas");
                baris[3] = data.getString("berat_total");
                baris[4] = data.getString("harga_awal");
                baris[5] = data.getString("tanggal_buka");
                baris[6] = data.getString("tanggal_habis");
                baris[7] = data.getString("waktu_habis");
                modelTabelBukaLelang.addRow(baris);   
            }
            cari.close();
            data.close();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_txtCariKeyPressed

