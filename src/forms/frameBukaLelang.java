    public void tampilDataBukaLelang() {
        try {
            modelTabelBukaLelang.getDataVector().removeAllElements();
            modelTabelBukaLelang.fireTableDataChanged();
            
            PreparedStatement query = myObject.konek.prepareStatement("SELECT * FROM buka_lelang");
            ResultSet data = query.executeQuery();
            while (data.next()) {                
                Object[] baris = new Object[8];
                baris[0] = data.getString("id_bukalelang");
                baris[1] = data.getString("id_petani");
                baris[2] = data.getString("id_komoditas");
                baris[3] = data.getString("berat_total");
                baris[4] = data.getString("harga_awal");
                baris[5] = data.getString("tanggal_buka");
                baris[6] = data.getString("tanggal_habis");
                baris[7] = data.getString("waktu_habis");
                modelTabelBukaLelang.addRow(baris);   
            }
            query.close();
            data.close();
        } catch (Exception e) {
        }
    }

